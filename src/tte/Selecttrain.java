    private void txttrainnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txttrainnoActionPerformed
        // TODO add your handling code here:
        String trainno = txttrainno.getSelectedItem().toString(); //Getting the train number input
        if(trainno.equals(""))  //Auto filling the train name
            txttrainname.setText("");
        if(trainno.equals("16127"))
        {
            txtcoach.removeAllItems();
            txttrainname.setText("Chennai Egmore - Guruvayur Express");   
        }
        if(trainno.equals("12631"))
        {
            txtcoach.removeAllItems();
            txttrainname.setText("Chennai Egmore - Tirunelveli Nellai SF Express");
        }
        if(trainno.equals("12633"))
        {
            txtcoach.removeAllItems();
            txttrainname.setText("Chennai Egmore - Kanniyakumari SF Express");
        }
        if(trainno.equals("16723"))
        {
            txtcoach.removeAllItems();
            txttrainname.setText("Chennai Egmore - Kollam Ananthapuri Express");
        }
        
        
        try {   //Adding the train coaches to the drop down using data from the traincoaches table in the database
            pst=con.prepareStatement("select coach from traincoaches where trainno = ?;");
            pst.setString(1,trainno);
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c=rsd.getColumnCount();
            
            txtcoach.addItem("");
            
            while(rs.next())
            {
                for(int i=0;i<c;i++)
                {
                    txtcoach.addItem(rs.getString("coach"));
                }
            }
        }catch (SQLException ex) {
            Logger.getLogger(Selecttrain.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Error");
        }
                    
    }//GEN-LAST:event_txttrainnoActionPerformed

