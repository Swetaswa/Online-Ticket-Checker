    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String trainno= txttrainno.getSelectedItem().toString();    //Getting the user inputs
        String coach= txtcoach.getSelectedItem().toString();
        Date date= txtdate.getDate();
        
        SimpleDateFormat format1 = new SimpleDateFormat("dd-MM-yyyy");  //Appropriate date format
        String newdate = format1.format(date).toString(); 
        Date date1= txtdate.getDate();
        Passengerdetails1 p= new Passengerdetails1();
        this.hide();
        p.txttrainno1.setText(trainno); //Setting the user inputs onto the passengerdetails form
        p.txtdate1.setText(newdate);
        p.txtcoachno1.setText(coach);
        p.txtdate2.setDate(date1);
        p.txtdate2.hide();
        p.jLabel6.setVisible(false);
        try {   //Geting the travelling passenger details from the passengerdetails table and setting it onto the JTable
            pst=con.prepareStatement("select Seatno,Name,Source,Destination,ID,Phoneno,Status from passengerdetails;");
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c=rsd.getColumnCount();
            
            DefaultTableModel d= (DefaultTableModel)p.bookingtable.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v=new Vector();
                for(int i=0;i<c;i++)
                {
                    v.add(rs.getString("Seatno"));
                    v.add(rs.getString("Name"));
                    v.add(rs.getString("Source"));
                    v.add(rs.getString("Destination"));
                    v.add(rs.getString("ID"));
                    v.add(rs.getString("Phoneno"));
                    v.add(rs.getString("Status"));
       
                }
                d.addRow(v);
            }
                p.setVisible(true);
                p.changeTable(p.bookingtable, 6);
        } catch (SQLException ex) {
            Logger.getLogger(Passengerdetails1.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Error");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

