    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        finedreport f= new finedreport(); ////Getting passengers who are fined from the fines table and setting it to the JTable
        try {
            pst=con.prepareStatement("select * from fines");
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c=rsd.getColumnCount();
            
            DefaultTableModel d= (DefaultTableModel)f.finetable.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v=new Vector();
                for(int i=0;i<c;i++)
                {
                    v.add(rs.getString("Sno"));
                    v.add(rs.getString("Cname"));
                    v.add(rs.getString("Reason"));
                    v.add(rs.getString("Amount"));
                }
                d.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Passengerdetails1.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Error");
        }
        
        f.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

