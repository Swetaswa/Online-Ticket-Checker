    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String trainno=txttrainno1.getText();   //Getting only passengers where status is checked and setting it to the JTable
            pst=con.prepareStatement("select Seatno,Name,Source,Destination,ID,Phoneno,Status from passengerdetails where Status='Checked';");
            rs=pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c=rsd.getColumnCount();
            
            DefaultTableModel d= (DefaultTableModel)reporttable.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v=new Vector();
                for(int i=0;i<c;i++)
                {
                    v.add(rs.getString("Seatno"));
                    v.add(rs.getString("Name"));
                    v.add(rs.getString("Source"));
                    v.add(rs.getString("Destination"));
                    v.add(rs.getString("ID"));
                    v.add(rs.getString("Phoneno"));
       
                }
                d.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Passengerdetails1.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

